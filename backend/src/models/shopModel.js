import mongoose from 'mongoose';

const Schema = mongoose.Schema;


export const ShopSchema = Schema({
   picture: {
       type: String,
       required: true
   },
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    city: {
        type: String,
        required: true
    },
    location: {
        type: {
            type: String,
            enum: ['Point'],
            required: true
        },
        coordinates: {
            type: [Number],
            required: true
        }
    }
});